def 材质分类(模型, 材质集合=None):

    # 1.1.0fbx模型分离
    if not 材质集合:
        材质集合 = 获取材质(模型)
        # 材质集合 = set()
        # def 添加材质(模型):
        #     for 材质 in 模型.data.materials:
        #         材质集合.add(材质)
        # # 添加材质(模型)
        # 完成递归 = set()
        # 骨架 = 模型.parent
        # # if 骨架 and 骨架.type == 'ARMATURE' and len([网格 for 网格 in 骨架.children if 网格.type == 'MESH']) > 1:
        # if 骨架:
        #     def 递归(骨架):
        #         if 骨架 not in 完成递归:
        #             完成递归.add(骨架)
        #             for 网格 in 骨架.children:
        #                 if 网格.type == 'MESH':  # 面部定位
        #                     添加材质(网格)
        #                 elif 网格.children:
        #                     for 物体 in 网格.children:
        #                         递归(物体)
        #             if 骨架.parent:
        #                 递归(骨架.parent)
        #     递归(骨架)

    脸 = next((材质 for 材质 in 材质集合 if any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 脸部关键词)), None)  # 1.0.3更新大小写匹配

    表情材质 = []
    头发材质 = []
    脸部材质 = []  # 有些模型将脸分成多个材质
    皮肤材质 = []
    五官材质 = []
    衣服材质 = []  # 反向过滤

    透明材质 = []  # 1.0.9检测透明材质  # 1.1.0改进为UV像素检测

    for 材质 in 材质集合:
        if 材质.小二预设模板.透明材质:
            透明材质.append(材质)
        if 材质.小二预设模板.材质分类:
            if   材质.小二预设模板.材质分类 == '脸部':
                脸部材质.append(材质)
            elif 材质.小二预设模板.材质分类 == '五官':
                五官材质.append(材质)
            elif 材质.小二预设模板.材质分类 == '表情':
                表情材质.append(材质)
            elif 材质.小二预设模板.材质分类 == '头发':
                头发材质.append(材质)
            elif 材质.小二预设模板.材质分类 == '皮肤':
                皮肤材质.append(材质)
            elif 材质.小二预设模板.材质分类 == '衣服':
                衣服材质.append(材质)
            continue  # 跳过后续判断

        是表情 = any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 表情关键词)
        是头发 = any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 头发关键词)
        是脸部 = any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 脸部关键词)
        是皮肤 = any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 皮肤关键词)
        是五官 = any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 五官关键词)
        是衣服 = any(关键词 in 材质.name or 关键词 in 材质.name.lower() for 关键词 in 衣服关键词)

        if   是衣服:
            衣服材质.append(材质)
            材质.小二预设模板.材质分类 = '衣服'
            材质.小二预设模板.更新分类 = '衣服'
            材质.小二预设模板.初始分类 = '衣服材质'
            continue  # 跳过后续判断
        if 是表情:
            表情材质.append(材质)
            材质.小二预设模板.材质分类 = '表情'
            材质.小二预设模板.更新分类 = '表情'
            材质.小二预设模板.初始分类 = '表情材质'
        elif 是头发:
            头发材质.append(材质)
            材质.小二预设模板.材质分类 = '头发'
            材质.小二预设模板.更新分类 = '头发'
            材质.小二预设模板.初始分类 = '头发材质'
        elif 是脸部:
            脸部材质.append(材质)
            材质.小二预设模板.材质分类 = '脸部'
            材质.小二预设模板.更新分类 = '脸部'
            材质.小二预设模板.初始分类 = '脸部材质'
        elif 是皮肤:
            皮肤材质.append(材质)
            材质.小二预设模板.材质分类 = '皮肤'
            材质.小二预设模板.更新分类 = '皮肤'
            材质.小二预设模板.初始分类 = '皮肤材质'
        elif 是五官:
            五官材质.append(材质)
            材质.小二预设模板.材质分类 = '五官'
            材质.小二预设模板.更新分类 = '五官'
            材质.小二预设模板.初始分类 = '五官材质'
        else:
            衣服材质.append(材质)
            材质.小二预设模板.材质分类 = '衣服'
            材质.小二预设模板.更新分类 = '衣服'
            材质.小二预设模板.初始分类 = '衣服材质'
    return 五官材质, 表情材质, 头发材质, 脸, 脸部材质, 皮肤材质, 衣服材质, 透明材质
