# coding: utf-8

import bpy
def 获取AO贴图(游戏, 基础贴图):
###################################################################################################
    if 游戏 == "绝区零":
        if "_D" in 基础贴图.name:
            AO贴图 = bpy.data.images[基础贴图.name.replace("_D", "_A")]
            return AO贴图
        else:
            return None
###################################################################################################
